// double vs 3 quals

// double equals check for value not type equality
let a = 1;
let b = "1";
console.log(a == b);
// whereas 3 equals check the value and data type
console.log(a === b);

// zero equals to empty string
console.log(0 == "");

//  0 = false, 1 = true
console.log(0 == false);
console.log(1 == true);

//  null = undefined
console.log(null == undefined);

// 3 equals
console.log(0 === "");

console.log(null === undefined);
console.log(null == null);

console.log(0 === false);
